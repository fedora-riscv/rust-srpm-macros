# Include Beaker environment
. /usr/share/beakerlib/beakerlib.sh || exit 1

PACKAGE="rust-srpm-macros"

rlJournalStart
    rlPhaseStartSetup
        rlAssertRpm $PACKAGE
        rlRun "TmpDir=\$(mktemp -d)" 0 "Creating tmp directory"
        rlRun "pushd $TmpDir"
    rlPhaseEnd

    rlPhaseStartTest
        # rust-srpm-macros should have set an RPM macro
        # %rust_arches. This test will check that the box
        # running is included in that macro
        rlRun "rpm -E %rust_arches > rust_arches.out"
        rlAssertExists "rust_arches.out"
        rlAssertGrep "$(uname -m)" rust_arches.out
    rlPhaseEnd

    rlPhaseStartCleanup
        rlRun "popd"
        rlRun "rm -r $TmpDir" 0 "Removing tmp directory"
    rlPhaseEnd
rlJournalPrintText
rlJournalEnd
